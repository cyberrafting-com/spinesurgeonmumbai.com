                            <h2>Diagnosis of Scoliosis in Children</h2>
                            <p>The diagnosis of scoliosis typically begins with a physical examination, where the doctor looks for signs such as uneven shoulders, hips, or posture. If scoliosis is suspected, imaging tests such as X-rays or MRIs are used to evaluate the severity of the curvature and to determine whether the spine is still growing.<br>
                            The degree of curvature is measured in degrees, and scoliosis is classified as follows:</p>
                            
                            <div class="cat-service-inner pt-5">
                                <img src="img/founder_li_icon.png" alt="">
