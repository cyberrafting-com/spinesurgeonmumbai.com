                            <li>Lumbar disc herniation</li>
                            <li>Cervical and lumbar spinal stenosis</li>
                            <li>Foraminal narrowing and nerve compression</li>
                            <li>Sciatica</li>
                            <li>Facet joint cysts</li>
                            <li>Recurrent disc prolapse</li>
                        </ul>

