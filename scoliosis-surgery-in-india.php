                        <h2>Understanding Scoliosis and Its Surgical Need</h2>
                        <p>Scoliosis refers to an abnormal lateral curvature of the spine that may progress over time if
                            left untreated. While mild curves can be monitored or managed conservatively, curves beyond
                            40–45 degrees often require surgical correction to prevent further progression, restore
                            spinal balance, and improve a patient’s overall quality of life.</p>

                        <p>Common symptoms in surgical cases include:</p>
                        <ul>
                            <li>-Uneven shoulders or hips</li>
                            <li>-Noticeable spinal curve</li>
                            <li>-Back pain or stiffness</li>
                            <li>-Reduced lung function in severe curves</li>
                            <li>-Difficulty with posture, walking, or balance</li>
                        </ul>

                        <p>Surgery is typically recommended when non-surgical methods like bracing or physiotherapy fail
                            to stop progression or when the curve begins to impair physical or psychological well-being.
