                        <h2>What Is a Slipped Disc?</h2>
                        <p>A slipped disc, also called <a href="herniated-disc.php">herniated disc</a> or prolapsed disc, occurs when the soft cushion of tissue between the vertebrae bulges out or ruptures, pressing on the spinal nerves. This can cause:</p>
                        <ul>
                            <li>-Severe back or neck pain</li>
                            <li>-Pain radiating down the arms or legs (sciatica)</li>
                            <li>-Tingling or numbness</li>
                            <li>-Muscle weakness</li>
                            <li>-Difficulty walking or standing for long periods</li>
                        </ul>
                        <p>While many cases respond well to medication and physiotherapy, surgery may be required if symptoms persist or worsen.</p>

                        <h2>When Is Slip Disc Surgery Needed?</h2>
                        <p>Dr. Vishal Kundnani recommends surgery only when it’s absolutely necessary. Some clear indicators for surgical treatment include:</p>
                        <ul>
                            <li>-No relief from conservative treatments after 4–6 weeks</li>
                            <li>-Progressive muscle weakness</li>
                            <li>-Loss of bladder or bowel control (cauda equina syndrome)</li>
                            <li>-Persistent leg or arm pain affecting daily life</li>
                        </ul>
                        <p>His detailed diagnostic process includes MRI, nerve conduction studies, and clinical evaluation to determine the best course of action.</p>

