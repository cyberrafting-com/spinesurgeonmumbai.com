            <h3 class="mt-2">What is the Anatomy of Back Spine ?</h3>
            <p class="mt-1">Human Spine is made of up 33 bones (vertebrae) that are cushioned by discs. These vertebrae
              are divided by region: neck (cervical spine), mid-back (thoracic spine), and low back (lumbar spine). At
              the lower end spine ends at the terminal bone of sacrum and the coccyx, which is commonly called your
              tailbone. Discs are present between these bones. The disks protect the bones by absorbing the shocks from
              daily activities like walking, lifting, and twisting. Each disk has two parts – a soft, gelatinous inner
              portion (nucleus pulp-ous) and a tough outer ring (annulus fibrosis). Facet joints are on the posterior
              side (back) of your vertebrae. These joints (like all joints in your body) help movement and are very
              important for flexibility. Together, the vertebrae and discs form a tunnel through which the spinal cords
              and nerve pass. The spine also has muscles, ligaments and blood vessels. Muscles are tissues that act as
              power generators for movement. Ligaments are the strong, flexible bands of fibrous tissue that link the
              bones together.</p>
