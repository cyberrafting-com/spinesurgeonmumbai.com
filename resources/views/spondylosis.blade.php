                        <h1>What is the affected anatomy in spondylosis ?</h1>
                        <p>Human Spine is made of up 33 bones (vertebrae) that are cushioned by discs. These vertebrae are
                            divided by region: neck (cervical spine), mid-back (thoracic spine), and low back (lumbar
                            spine). At the lower end spine ends at the terminal bone of sacrum and the coccyx, which is
                            commonly called your tailbone. Discs are present between these bones. The disks protect the
                            bones by absorbing the shocks from daily activities like walking, lifting, and twisting. Each
                            disk has two parts – a soft, gelatinous inner portion (nucleus pulpous) and a tough outer ring
                            (annulus fibrosis). Facet joints are on the posterior side (back) of your vertebrae. These
                            joints (like all joints in your body) help movement and are very important for flexibility .
                            Spondylosis that affects the joints is called osteoarthritis, which is the most common form of
                            arthritis in the spine. These spinal joints are covered by cartilage. With erosion of the
                            cartilage, as occurs with age, the bones rub together-this causes severe pain.</p>

