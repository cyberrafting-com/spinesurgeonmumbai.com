                        <p>मानव रीढ़ 33 हड्डियों (कशेरुका) से बनी होती है जो डिस्क द्वारा कुशन की जाती हैं। ये कशेरुका क्षेत्र द्वारा विभाजित होती हैं: गर्दन (सर्वाइकल स्पाइन), मध्य-पीठ (थोरैसिक स्पाइन), और कमर (लम्बर स्पाइन)। निचले सिरे पर रीढ़ सैक्रम की अंतिम हड्डी और कोक्सीक्स पर समाप्त होती है, जिसे आमतौर पर आपकी टेलबोन कहा जाता है। इन हड्डियों के बीच डिस्क मौजूद होती हैं। डिस्क चलने, उठाने और मोड़ने जैसी दैनिक गतिविधियों से झटके को अवशोषित करके हड्डियों की रक्षा करती हैं। प्रत्येक डिस्क के दो भाग होते हैं – एक नरम, जिलेटिनस आंतरिक हिस्सा (न्यूक्लियस पल्पोसस) और एक कठोर बाहरी रिंग (एन्युलस फाइब्रोसिस)। फेसेट जोड़ आपकी कशेरुका के पीछे की तरफ (पीठ) पर होते हैं। ये जोड़ (आपके शरीर में सभी जोड़ों की तरह) आंदोलन में मदद करते हैं और लचीलेपन के लिए बहुत महत्वपूर्ण हैं। जोड़ों को प्रभावित करने वाले स्पोंडिलोसिस को ऑस्टियोआर्थराइटिस कहा जाता है, जो रीढ़ में गठिया का सबसे आम रूप है। ये स्पाइनल जोड़ उपास्थि से ढके होते हैं। उपास्थि के क्षरण के साथ, जैसा कि उम्र के साथ होता है, हड्डियां एक साथ रगड़ती हैं—यह गंभीर दर्द का कारण बनता है।</p>

