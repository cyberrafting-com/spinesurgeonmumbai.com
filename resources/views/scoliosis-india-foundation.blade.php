                        <h3>Bracing</h3>
                        <p>Person with scoliosis may need to use a brace if they're still growing and the curvature is more
                            than 25 to 40 degrees.</p>
                        <p>Braces won't straighten the spine, but they can prevent the curvature from increasing. This
                            method of treatment is more effective for cases that are detected early.</p>
                        <p>Those requiring a brace need to wear it 16 to 23 hours a day until they stop growing. A brace's
                            effectiveness increases with the number of hours they wear it per day.</p>
                        <p>Doctors usually recommend that children wear their braces until they reach adolescence and are no
                            longer growing.</p>
                        <h3>Surgery</h3>
                        <p>Surgery is usually reserved for people with curves greater than 40 degrees. However, talk to your
                            doctor about this option if you've been diagnosed with scoliosis and feel the curvature is
                            interrupting your daily life or causing you discomfort.</p>
