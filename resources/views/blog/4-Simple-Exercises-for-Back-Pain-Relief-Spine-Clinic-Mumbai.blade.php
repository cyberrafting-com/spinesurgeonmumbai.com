                            <h2><b>4 Simple Exercises for Back Pain Relief</b></h2>

                            <h3 class="pt-3"><b>1. Pelvic Tilts</b></h3>
                            <p>Pelvic tilts help strengthen the lower back and core muscles, which are essential for providing support to the spine.</p>
                            <p><b>How to do it:</b></p>
                            <ul>
                                <li style="list-style-type: disc; padding: 5px 0;">Lie on your back with your knees bent and feet flat on the floor.</li>
                                <li style="list-style-type: disc; padding: 5px 0;">Tighten your abdominal muscles and push your lower back into the floor.</li>
                                <li style="list-style-type: disc; padding: 5px 0;">Hold for 5 seconds, then relax.</li>
                                <li style="list-style-type: disc; padding: 5px 0;">Repeat for 10-15 repetitions.</li>
                            </ul>

                            <h3 class="pt-3"><b>2. Cat-Cow Stretch</b></h3>
                            <p>This yoga-inspired stretch helps to increase flexibility in your spine and relieve stiffness in your back muscles.</p>
                            <p><b>How to do it:</b></p>
                            <ul>
                                <li style="list-style-type: disc; padding: 5px 0;">Start on all fours with your hands directly under your shoulders and knees under your hips.</li>
                                <li style="list-style-type: disc; padding: 5px 0;">Inhale and arch your back (drop your belly towards the floor), lifting your head and tailbone (Cow position).</li>
                                <li style="list-style-type: disc; padding: 5px 0;">Exhale and round your back, tucking your chin to your chest and pushing your spine toward the ceiling (Cat position).</li>
                                <li style="list-style-type: disc; padding: 5px 0;">Repeat 10-15 times, moving slowly and fluidly.</li>
                            </ul>

                            <h3 class="pt-3"><b>3. Bridge Exercise</b></h3>
                            <p>The bridge exercise targets your glutes, lower back, and core muscles, all of which help support proper posture and spinal alignment.</p>
                            <p><b>How to do it:</b></p>
                            <ul>
                                <li style="list-style-type: disc; padding: 5px 0;">Lie on your back with your knees bent and feet flat on the floor, hip-width apart.</li>
                                <li style="list-style-type: disc; padding: 5px 0;">Lift your hips towards the ceiling, engaging your glutes and core muscles.</li>
                                <li style="list-style-type: disc; padding: 5px 0;">Hold for 5 seconds, then lower your hips back to the floor.</li>
                                <li style="list-style-type: disc; padding: 5px 0;">Repeat for 10-15 repetitions.</li>
                            </ul>

                            <h3 class="pt-3"><b>4. Child's Pose</b></h3>
                            <p>This gentle stretch helps release tension in the back and promotes relaxation. It's a great way to counteract the strain caused by sitting for long periods.</p>
                            <p><b>How to do it:</b></p>
                            <ul>
                                <li style="list-style-type: disc; padding: 5px 0;">Start on all fours with your hands and knees on the floor.</li>
                                <li style="list-style-type: disc; padding: 5px 0;">Slowly lower your hips back towards your heels while reaching your arms forward.</li>
                                <li style="list-style-type: disc; padding: 5px 0;">Hold the position for 20-30 seconds, then return to the starting position.</li>
                                <li style="list-style-type: disc; padding: 5px 0;">Repeat 3-5 times.</li>
                            </ul>
