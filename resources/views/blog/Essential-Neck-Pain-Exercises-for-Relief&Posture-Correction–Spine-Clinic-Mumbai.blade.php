                            <h2><b>Essential Neck Pain Exercises for Relief</b></h2>
                            <p>There are several exercises you can do at home or work to ease neck pain. These exercises not only relieve discomfort but also improve the strength and flexibility of your neck muscles.</p>
                            
                            <h3 class="pt-3"><b>1. Neck Stretches</b></h3>
                            <p>Neck stretches help to improve flexibility and reduce stiffness in the neck muscles.</p>
                            <ul>
                                <li style="list-style-type: disc; padding: 5px 0;">Neck Tilt Stretch: Sit upright and tilt your head toward your shoulder. Hold for 20-30 seconds and repeat on the other side.</li>
                                <li style="list-style-type: disc; padding: 5px 0;">Neck Rotation: Slowly rotate your head to one side, hold for 5 seconds, and return to center. Repeat for the other side.</li>
                            </ul>

                            <h3 class="pt-3"><b>2. Chin Tucks</b></h3>
                            <p>Chin tucks are effective in strengthening the muscles that support proper neck alignment. They help correct forward head posture, which is common for people who spend long hours in front of screens.</p>
                            <p>How to do it: Sit or stand up straight. Tuck your chin toward your chest and hold for 5 seconds. Repeat 10-15 times.</p>

                            <h3 class="pt-3"><b>3. Shoulder Blade Squeeze</b></h3>
                            <p>This exercise targets the muscles around the neck and upper back, helping to reduce tension and promote better posture.</p>
                            <p>How to do it: Sit or stand up straight. Squeeze your shoulder blades together, hold for 5 seconds, and relax. Repeat 10-15 times.</p>

                            <h3 class="pt-3"><b>4. Posture Correction Techniques</b></h3>
                            <p>Maintaining good posture is crucial for preventing neck pain in the long run. Practice these techniques to ensure proper alignment:</p>
                            <ul>
                                <li style="list-style-type: disc; padding: 5px 0;">Sit Upright: When sitting, keep your back straight, shoulders relaxed, and ears aligned with your shoulders.</li>
                                <li style="list-style-type: disc; padding: 5px 0;">Stand Tall: When standing, distribute your weight evenly on both feet, engage your core, and keep your shoulders back.</li>
                            </ul>
