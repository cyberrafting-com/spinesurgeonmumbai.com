                        <h1>What is Kyphosis ?</h1>
                        <p class="mt-1">The word "kyphosis" describes a type of curve in the spine. A kyphotic curve is
                            'normally' present in the thoracic spine (the part of the spine in the chest area). A kyphotic
                            curve looks like the letter "C" with the opening of the C pointing towards the front. Though the
                            thoracic spine is supposed to be curved, if the curve in a person's thoracic spine is more than
                            40 to 45 degrees, it is considered abnormal – or a spinal deformity.</p>
                        <p><b>Normal Anatomy</b></p>
                        <p>The thoracic spine naturally has some kyphosis. A kyphotic spinal deformity really means there is
                            too much kyphotic curve in the spine.The thoracic spine is made up of the middle 12 vertebrae of
                            your spine. The normal amount of curve in the thoracic spine is considered to be from 20 to 40
                            degrees within the entire thoracic spine. There is a range because the amount of "normal" curve
                            varies from person to person</p>
                        <p>Kyphosis can have varying symptoms and degrees of severity, from minor changes in the shape of
                            back, to severe deformity, nerve problems, and chronic pain.</p>
