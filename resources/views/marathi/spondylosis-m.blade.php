                        <p>मानवी मणका 33 हाडांनी (कशेरुका) बनलेली आहे जी डिस्कद्वारे कुशन केली जातात. या कशेरुका प्रदेशानुसार विभाजित केल्या जातात: मान (ग्रीवा मणका), मध्य-पाठ (वक्ष मणका), आणि खालची पाठ (कटीचा मणका). खालच्या टोकाला मणका सॅक्रमच्या अंतिम हाडावर आणि कोक्सीक्सवर समाप्त होते, ज्याला सामान्यतः तुमची शेपटी म्हणतात. या हाडांमध्ये डिस्क असतात. डिस्क चालणे, उचलणे आणि वळवणे यासारख्या दैनंदिन क्रियाकलापांपासून धक्के शोषून हाडांचे संरक्षण करतात. प्रत्येक डिस्कचे दोन भाग असतात – एक मऊ, जेलॅटिनस आतील भाग (न्यूक्लियस पल्पोसस) आणि एक कठीण बाहेरील रिंग (अॅन्युलस फायब्रोसिस). फेसेट जॉइंट्स तुमच्या कशेरुकाच्या मागील बाजूला (पाठ) असतात. हे जोड (तुमच्या शरीरातील सर्व जोडांप्रमाणे) हालचालीला मदत करतात आणि लवचिकतेसाठी अत्यंत महत्त्वाचे आहेत. जोड प्रभावित करणाऱ्या स्पॉन्डिलोसिसला ऑस्टियोआर्थरायटिस म्हणतात, जो मणक्यातील संधिवाताचा सर्वात सामान्य प्रकार आहे. हे स्पाइनल जोड कार्टिलेजने झाकलेले असतात. कार्टिलेजच्या क्षरणासह, जसे वयानुसार होते, हाडे एकमेकांना घासतात—यामुळे तीव्र वेदना होतात.</p>

