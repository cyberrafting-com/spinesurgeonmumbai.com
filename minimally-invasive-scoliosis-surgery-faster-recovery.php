                        <ul>
                            <li><strong>X-rays:</strong> To assess the degree of curvature and the health of the spine.</li>
                            <li><strong>MRI:</strong> To evaluate the spinal cord and nerves, ensuring there are no underlying issues that could affect the surgery.</li>
                            <li><strong>Clinical Assessment:</strong> The doctor will assess the patient's overall health, age, and growth potential to determine the most suitable approach to surgery.</li>
                        </ul>
